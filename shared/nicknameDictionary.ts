/**
 * 从词库随机生成一个4字昵称
 */
export function generateRandomNickname(): string {
  const prefix = NICKNAME_PREFIXES[Math.floor(Math.random() * NICKNAME_PREFIXES.length)];
  const suffix = NICKNAME_SUFFIXES[Math.floor(Math.random() * NICKNAME_SUFFIXES.length)];
  return prefix + suffix;
}

/**
 * 生成多个候选昵称（用于前端摇骰子）
 */
export function generateNicknameCandidates(count: number = 5): string[] {
  const results: string[] = [];
  const used = new Set<string>();
  let attempts = 0;
  while (results.length < count && attempts < count * 10) {
    const name = generateRandomNickname();
    if (!used.has(name)) {
      used.add(name);
      results.push(name);
    }
    attempts++;
  }
  return results;
}
